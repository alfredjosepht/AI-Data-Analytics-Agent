class NumberedCanvas(canvas.Canvas):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self._saved_page_states = []

    def showPage(self):
        self._saved_page_states.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        num_pages = len(self._saved_page_states)
        for state in self._saved_page_states:
            self.__dict__.update(state)
            self.draw_page_decorations(num_pages)
            super().showPage()
        super().save()

    def draw_page_decorations(self, page_count):
        self.saveState()
        self.setFont("Helvetica", 8)
        self.setFillColor(colors.HexColor('#64748b'))
        
        # Header (Only on page 2 and later)
        if self._pageNumber > 1:
            self.setStrokeColor(colors.HexColor('#e2e8f0'))
            self.setLineWidth(0.5)
            self.line(36, 756, 576, 756)
            self.drawString(36, 762, "AI Data Analytics Agent — Executive Report")
            self.drawRightString(576, 762, datetime.now().strftime('%Y-%m-%d'))
        
        # Footer (On all pages)
        self.setStrokeColor(colors.HexColor('#e2e8f0'))
        self.setLineWidth(0.5)
        self.line(36, 54, 576, 54)
        page_text = f"Page {self._pageNumber} of {page_count}"
        self.drawRightString(576, 40, page_text)
        self.drawString(36, 40, "Confidential • Generated by AI Data Analytics Agent")
        self.restoreState()


@router.get("/workspaces/{workspace_id}/export/pdf")
def export_pdf(workspace_id: int):
    ws = sqlite_manager.get_workspace(workspace_id)
    if not ws:
        raise HTTPException(status_code=404, detail="Workspace not found")

    if SimpleDocTemplate is None:
        raise HTTPException(status_code=500, detail="reportlab is not fully installed.")

    out_dir = "exports"
    os.makedirs(out_dir, exist_ok=True)
    out_path = os.path.join(out_dir, f"workspace_{workspace_id}.pdf")

    doc = SimpleDocTemplate(out_path, pagesize=letter, rightMargin=36, leftMargin=36, topMargin=54, bottomMargin=54)
    styles = getSampleStyleSheet()
    
    title_style = ParagraphStyle(
        'DocTitle',
        parent=styles['Heading1'],
        fontSize=24,
        leading=28,
        textColor=colors.HexColor('#1e293b'),
        spaceAfter=15
    )
    section_style = ParagraphStyle(
        'SectionHeader',
        parent=styles['Heading2'],
        fontSize=13,
        leading=16,
        textColor=colors.HexColor('#2563eb'),
        spaceBefore=14,
        spaceAfter=6,
        keepWithNext=True
    )
    body_style = ParagraphStyle(
        'DocBody',
        parent=styles['Normal'],
        fontSize=9,
        leading=13,
        textColor=colors.HexColor('#334155'),
        spaceAfter=6
    )
    code_style = ParagraphStyle(
        'DocCode',
        parent=styles['Code'],
        fontSize=8,
        leading=11,
        textColor=colors.HexColor('#0f172a'),
        backColor=colors.HexColor('#f1f5f9'),
        borderColor=colors.HexColor('#cbd5e1'),
        borderWidth=0.5,
        borderPadding=6,
        spaceAfter=8
    )

    elements = []
    temp_images = []

    # 1. Cover / Metadata Section
    elements.append(Paragraph(f"Enterprise Analytics Report", title_style))
    elements.append(Paragraph(f"<b>Workspace:</b> {ws.get('name').upper()}", body_style))
    elements.append(Paragraph(f"<b>Dataset File:</b> {ws.get('file_name')}", body_style))
    elements.append(Paragraph(f"<b>Generated At:</b> {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}", body_style))
    elements.append(Spacer(1, 15))

    # Dataset Summary Section
    elements.append(Paragraph("Dataset Profile Summary", section_style))
    meta = ws.get('metadata') or {}
    row_count = meta.get('row_count', 'N/A')
    col_count = meta.get('column_count', 'N/A')
    cols = meta.get('columns', [])
    elements.append(Paragraph(f"The dataset contains <b>{row_count}</b> rows and <b>{col_count}</b> columns.", body_style))
    elements.append(Paragraph(f"<b>Columns Profiled:</b> {', '.join(cols)}", body_style))
    elements.append(Spacer(1, 10))

    # Data Quality Summary
    elements.append(Paragraph("Data Cleaning Operations Log", section_style))
    cleaning_report = ws.get('cleaning_report') or {}
    report_desc = cleaning_report.get('report', 'No automated cleaning requested.')
    approved = "Yes" if ws.get('cleaning_approved') else "No (Pending User Confirmation)"
    elements.append(Paragraph(f"<b>Cleaning Actions Confirmed:</b> {approved}", body_style))
    elements.append(Paragraph(f"<b>Operation Logs:</b> {report_desc}", body_style))
    elements.append(Spacer(1, 15))

    # 2. Iterate Queries & Insights
    chats = sqlite_manager.get_chat_history(workspace_id)
    has_charts = False
    
    if chats:
        elements.append(Paragraph("AI-Generated Insights & Visualizations", section_style))
        for idx, chat in enumerate(chats):
            elements.append(Paragraph(f"<b>Analysis Request {idx + 1}:</b> {chat.get('question')}", body_style))
            if chat.get('sql_query'):
                elements.append(Paragraph(f"Generated SQL Query:", body_style))
                elements.append(Paragraph(chat.get('sql_query'), code_style))
            elements.append(Paragraph(f"<b>AI Insight:</b> {chat.get('answer')}", body_style))
            
            # Embed Chart if exists
            chart_json_str = chat.get('chart_json')
            result_json_str = chat.get('result_json')
            if chart_json_str and result_json_str:
                try:
                    chart_cfg = json.loads(chart_json_str)
                    result_data = json.loads(result_json_str)
                    
                    if result_data and chart_cfg:
                        rec = chart_cfg.get("recommendation", {})
                        c_type = rec.get("recommended", "bar")
                        x_col = rec.get("x")
                        y_col = rec.get("y")
                        
                        img_path = ChartExporter.generate_chart_image(
                            data_list=result_data,
                            chart_type=c_type,
                            x_col=x_col,
                            y_col=y_col
                        )
                        if img_path and os.path.exists(img_path):
                            temp_images.append(img_path)
                            elements.append(Spacer(1, 5))
                            elements.append(Image(img_path, width=420, height=252))
                            elements.append(Spacer(1, 10))
                            has_charts = True
                except Exception as ex:
                    print(f"Failed to embed chart in PDF: {ex}")
            elements.append(Spacer(1, 15))
    else:
        # Fallback dataset preview
        table_name = ws.get('table_name')
        if table_name:
            elements.append(Paragraph("Dataset Sample Preview (Top 10 Rows)", section_style))
            try:
                # Query table preview
                first_tbl = [t.strip() for t in table_name.split(",") if t.strip()][0]
                df = duckdb_manager.query(f"SELECT * FROM {first_tbl} LIMIT 10")
                data_list = [list(df.columns)]
                for _, row in df.iterrows():
                    data_list.append([str(val)[:15] for val in row])
                
                col_w = 540.0 / len(df.columns)
                col_widths = [col_w] * len(df.columns)
                t = Table(data_list, colWidths=col_widths)
                t.setStyle(TableStyle([
                    ('BACKGROUND', (0,0), (-1,0), colors.HexColor('#1e293b')),
                    ('TEXTCOLOR', (0,0), (-1,0), colors.whitesmoke),
                    ('ALIGN', (0,0), (-1,-1), 'LEFT'),
                    ('FONTNAME', (0,0), (-1,0), 'Helvetica-Bold'),
                    ('FONTSIZE', (0,0), (-1,-1), 8),
                    ('BOTTOMPADDING', (0,0), (-1,-1), 4),
                    ('TOPPADDING', (0,0), (-1,-1), 4),
                    ('GRID', (0,0), (-1,-1), 0.5, colors.HexColor('#cbd5e1')),
                    ('ROWBACKGROUNDS', (0,1), (-1,-1), [colors.HexColor('#f8fafc'), colors.HexColor('#ffffff')])
                ]))
                elements.append(t)
            except Exception as e:
                elements.append(Paragraph(f"Could not load data preview: {e}", body_style))

    try:
        doc.build(elements, canvasmaker=NumberedCanvas)
        return FileResponse(out_path, media_type="application/pdf", filename=f"{ws.get('name')}_report.pdf")
    finally:
        # Clean up temp charts
        for img in temp_images:
            if os.path.exists(img):
                try:
                    os.remove(img)
                except OSError:
                    pass


@router.get("/workspaces/{workspace_id}/export/pptx")
def export_pptx(workspace_id: int):
    ws = sqlite_manager.get_workspace(workspace_id)
    if not ws:
        raise HTTPException(status_code=404, detail="Workspace not found")

    if Presentation is None:
        raise HTTPException(status_code=500, detail="python-pptx is not installed.")

    out_dir = "exports"
    os.makedirs(out_dir, exist_ok=True)
    out_path = os.path.join(out_dir, f"workspace_{workspace_id}.pptx")

    prs = Presentation()
    temp_images = []

    def apply_slide_title(slide, text):
        title = slide.shapes.title
        title.text = text
        for p in title.text_frame.paragraphs:
            p.font.size = Pt(28)
            p.font.bold = True
            p.font.color.rgb = RGBColor(30, 41, 59)

    # Slide 1: Cover Page
    slide1 = prs.slides.add_slide(prs.slide_layouts[0])
    slide1.shapes.title.text = ws.get('name').upper()
    slide1.placeholders[1].text = f"Business Intelligence Dashboard Report\nDataset: {ws.get('file_name')}\nDate: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}"

    # Slide 2: Dataset Profile & Quality
    slide2 = prs.slides.add_slide(prs.slide_layouts[1])
    apply_slide_title(slide2, "Dataset Profile & Quality")
    tb2 = slide2.shapes.placeholders[1].text_frame
    meta = ws.get('metadata') or {}
    tb2.text = f"• Total Columns: {meta.get('column_count', 'N/A')}"
    tb2.add_paragraph().text = f"• Total Rows: {meta.get('row_count', 'N/A')}"
    cleaning_report = ws.get('cleaning_report') or {}
    report_desc = cleaning_report.get('report', 'No cleaning operations applied.')
    tb2.add_paragraph().text = f"• Cleaning Operations Log: {report_desc}"

    # Slide 3-N: Chats, Insights, and Charts
    chats = sqlite_manager.get_chat_history(workspace_id)
    if chats:
        for idx, chat in enumerate(chats[:5]): # Limit to top 5 queries for slides
            slide = prs.slides.add_slide(prs.slide_layouts[5]) # Blank with title
            apply_slide_title(slide, f"Query {idx + 1}: {chat.get('question')[:40]}...")
            
            # Left pane: SQL and Insight text
            left_box = slide.shapes.add_textbox(Inches(0.5), Inches(1.5), Inches(5.0), Inches(5.0))
            tf = left_box.text_frame
            tf.word_wrap = True
            
            p_q = tf.paragraphs[0]
            p_q.text = f"Query: {chat.get('question')}"
            p_q.font.bold = True
            p_q.font.size = Pt(12)
            
            if chat.get('sql_query'):
                p_sql = tf.add_paragraph()
                p_sql.text = f"\nSQL:\n{chat.get('sql_query')}"
                p_sql.font.name = 'Courier New'
                p_sql.font.size = Pt(9)
                p_sql.font.color.rgb = RGBColor(100, 116, 139)
            
            p_ans = tf.add_paragraph()
            p_ans.text = f"\nAI Executive Insight:\n{chat.get('answer')}"
            p_ans.font.size = Pt(11)
            p_ans.font.color.rgb = RGBColor(51, 65, 85)

            # Right pane: Embed chart image if available
            chart_json_str = chat.get('chart_json')
            result_json_str = chat.get('result_json')
            if chart_json_str and result_json_str:
                try:
                    chart_cfg = json.loads(chart_json_str)
                    result_data = json.loads(result_json_str)
                    if result_data and chart_cfg:
                        rec = chart_cfg.get("recommendation", {})
                        c_type = rec.get("recommended", "bar")
                        x_col = rec.get("x")
                        y_col = rec.get("y")
                        
                        img_path = ChartExporter.generate_chart_image(
                            data_list=result_data,
                            chart_type=c_type,
                            x_col=x_col,
                            y_col=y_col
                        )
                        if img_path and os.path.exists(img_path):
                            temp_images.append(img_path)
                            slide.shapes.add_picture(img_path, Inches(5.8), Inches(1.5), Inches(3.8), Inches(4.5))
                except Exception as ex:
                    print(f"Failed to embed chart in pptx: {ex}")
    else:
        slide = prs.slides.add_slide(prs.slide_layouts[1])
        apply_slide_title(slide, "AI Analytics Insights")
        slide.shapes.placeholders[1].text_frame.text = "No questions have been run on this workspace yet. Execute a query to view slides."

    try:
        prs.save(out_path)
        return FileResponse(out_path, media_type="application/vnd.openxmlformats-officedocument.presentationml.presentation", filename=f"{ws.get('name')}_presentation.pptx")
    finally:
        for img in temp_images:
            if os.path.exists(img):
                try:
                    os.remove(img)
                except OSError:
                    pass


