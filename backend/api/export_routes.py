from pydantic import BaseModel

class ExportReportRequest(BaseModel):
    include_dataset_summary: bool = True
    include_query_results: bool = True
    include_sql: bool = True
    include_insights: bool = True
    include_charts: bool = True
    include_tables: bool = True
    scope: str = "session"  # "current" or "session"
    format: str = "pdf"     # "pdf", "pptx", "excel"


@router.post("/workspaces/{workspace_id}/export_report")
def export_report_wizard(workspace_id: int, request: ExportReportRequest):
    ws = sqlite_manager.get_workspace(workspace_id)
    if not ws:
        raise HTTPException(status_code=404, detail="Workspace not found")
        
    chats = sqlite_manager.get_chat_history(workspace_id)
    if request.scope == "current":
        chats = chats[-1:] if chats else []
        
    out_dir = "exports"
    os.makedirs(out_dir, exist_ok=True)
    
    if request.format == "pdf":
        return generate_custom_pdf(ws, chats, request, workspace_id, out_dir)
    elif request.format == "pptx":
        return generate_custom_pptx(ws, chats, request, workspace_id, out_dir)
    elif request.format == "excel":
        return generate_custom_excel(ws, chats, request, workspace_id, out_dir)
    else:
        raise HTTPException(status_code=400, detail="Invalid format specified")


